# Python Operators
# -----------------

# 1. Assignment Operators
# Assign values to variables.
# Examples:
#   =   : Assigns value
#   +=  : Adds and assigns
#   -=  : Subtracts and assigns
#   *=  : Multiplies and assigns
#   /=  : Divides and assigns
#   %=  : Modulus and assigns
#   //= : Floor divides and assigns
#   **= : Exponentiates and assigns
#   &=  : Bitwise AND and assigns
#   |=  : Bitwise OR and assigns
#   ^=  : Bitwise XOR and assigns
#   >>= : Bitwise right shift and assigns
#   <<= : Bitwise left shift and assigns

# 2. Arithmetic Operators
# Perform mathematical operations.
# Examples:
#   +  : Addition
#   -  : Subtraction
#   *  : Multiplication
#   /  : Division
#   %  : Modulus (remainder)
#   // : Floor division
#   ** : Exponentiation
# 3. Comparison Operators
# Compare values and return True/False.
# Examples:
#   == : Equal to
#   != : Not equal to
#   >  : Greater than
#   <  : Less than
#   >= : Greater than or equal to
#   <= : Less than or equal to

# 4. Logical Operators
# Combine conditional statements.
# Examples:
#   and : True if both are True
#   or  : True if at least one is True
#   not : True if operand is False

# 5. Bitwise Operators
# Operate on binary representations.
# Examples:
#   &  : Bitwise AND
#   |  : Bitwise OR
#   ^  : Bitwise XOR
#   ~  : Bitwise NOT
#   << : Bitwise left shift
#   >> : Bitwise right shift

# 6. Membership Operators
# Test membership in a sequence.
# Examples:
#   in     : True if value is present
#   not in : True if value is not present

# 7. Identity Operators
# Compare memory locations.
# Examples:
#   is     : True if same object
#   is not : True if not same object
